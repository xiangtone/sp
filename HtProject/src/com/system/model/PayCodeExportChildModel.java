	private String orders;
	private String troneNum;
	private int price;
	
	public int getPayCode()
	{
		return payCode;
	}
	public void setPayCode(int payCode)
	{
		this.payCode = payCode;
	}
	public String getOrders()
	{
		return orders;
	}
