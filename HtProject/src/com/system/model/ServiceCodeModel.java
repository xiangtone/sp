package com.system.model;

public class ServiceCodeModel
{
	private int id;
	private int operatorBjFlag;
	private String serviceName;
	
	public int getId()
	{
		return id;
	}
	public void setId(int id)
	{
		this.id = id;
	}
	public int getOperatorBjFlag()
	{
		return operatorBjFlag;
	}
	public void setOperatorBjFlag(int operatorBjFlag)
	{
		this.operatorBjFlag = operatorBjFlag;
	}
	public String getServiceName()
	{
		return serviceName;
	}
	public void setServiceName(String serviceName)
	{
		this.serviceName = serviceName;
	}
}
