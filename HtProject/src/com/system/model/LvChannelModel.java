package com.system.model;

public class LvChannelModel
{
	private int id;
	private String appKey;
	private String channel;
	private int holdPercent;
	private int user_id;
	private int fenCheng;
	private int price;
	private int userHold;
	
	public int getId()
	{
		return id;
	}
	public void setId(int id)
	{
		this.id = id;
	}
	public String getAppKey()
	{
		return appKey;
	}
	public void setAppKey(String appKey)
	{
		this.appKey = appKey;
	}
	public String getChannel()
	{
		return channel;
	}
	public void setChannel(String channel)
	{
		this.channel = channel;
	}
	public int getHoldPercent()
	{
		return holdPercent;
	}
	public void setHoldPercent(int holdPercent)
	{
		this.holdPercent = holdPercent;
	}
	public int getUser_id()
	{
		return user_id;
	}
	public void setUser_id(int user_id)
	{
		this.user_id = user_id;
	}
	public int getFenCheng()
	{
		return fenCheng;
	}
	public void setFenCheng(int fenCheng)
	{
		this.fenCheng = fenCheng;
	}
	public int getPrice()
	{
		return price;
	}
	public void setPrice(int price)
	{
		this.price = price;
	}
	public int getUserHold()
	{
		return userHold;
	}
	public void setUserHold(int userHold)
	{
		this.userHold = userHold;
	}
	
	
}
