package com.system.model;

public class AdCpModel {
	private String feeDate;
	private String appname;
	private int newUserRows;
	private double amount;
	private double extendFee;
	private int id;
	public String getFeeDate() {
		return feeDate;
	}
	public void setFeeDate(String feeDate) {
		this.feeDate = feeDate;
	}
	public String getAppname() {
		return appname;
	}
	public void setAppname(String appname) {
		this.appname = appname;
	}
	public int getNewUserRows() {
		return newUserRows;
	}
	public void setNewUserRows(int newUserRows) {
		this.newUserRows = newUserRows;
	}
	public double getAmount() {
		return amount;
	}
	public void setAmount(double amount) {
		this.amount = amount;
	}
	public double getExtendFee() {
		return extendFee;
	}
	public void setExtendFee(double extendFee) {
		this.extendFee = extendFee;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	
}
